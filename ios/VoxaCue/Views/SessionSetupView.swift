    private var preparedDeckPlan: DeckPlan? {
        guard sessionMode == .powerPoint, !importedSlides.isEmpty else { return nil }
        let allocation: DeckTimingAllocation
        switch timingMode {
        case .even:
            allocation = .even(totalSeconds: targetDurationSeconds)
        case .perSlide:
            guard customSlideDurations.count == importedSlides.count else { return nil }
            allocation = .perSlide(
                durations: zip(importedSlides, customSlideDurations).map { slide, duration in
                    DeckSlideDuration(slideIndex: slide.index, durationSeconds: duration)
                },
                totalSeconds: targetDurationSeconds
            )
        }
        return try? buildTimedDeckPlan(
            title: importedFileName ?? "Presentation",
            slides: importedSlides,
            allocation: allocation
        )
    }

