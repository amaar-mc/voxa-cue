    private func handlePresentationImport(_ result: Result<[URL], Error>) {
        switch result {
        case let .success(urls):
            guard let url = urls.first else { return }
            importPresentation(url: url)
        case .failure:
            presentationImportError = presentationImportFailureMessage(
                errorDescription: "The file picker could not open that presentation.",
                retainsCurrentPresentation: !importedSlides.isEmpty
            )
        }
    }

    private func importPresentation(url: URL) {
        presentationImportError = nil
        isImportingPresentation = true
        Task {
            let hasSecurityAccess = url.startAccessingSecurityScopedResource()
            defer {
                if hasSecurityAccess { url.stopAccessingSecurityScopedResource() }
            }
            do {
                let slides = try await Task.detached(priority: .userInitiated) {
                    try PresentationFileParser(limits: .production).parse(url: url)
                }.value
                let nextGeneratedName = url.deletingPathExtension().lastPathComponent
                importedSlides = slides
                importedFileName = nextGeneratedName
                if name == "Practice Session"
                    || name.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty
                    || name == autoGeneratedSessionName {
                    name = nextGeneratedName
                    autoGeneratedSessionName = nextGeneratedName
                }
                if targetDurationSeconds < slides.count {
                    timingMode = .even
                }
                resetCustomTimingEvenly()
                isImportingPresentation = false
                presentationImportError = nil
                AccessibilityNotification.Announcement("Presentation imported. \(slides.count) slides.").post()
            } catch let error as PresentationParserError {
                isImportingPresentation = false
                presentationImportError = presentationImportFailureMessage(
                    errorDescription: error.localizedDescription,
                    retainsCurrentPresentation: !importedSlides.isEmpty
                )
                AccessibilityNotification.Announcement("Presentation import failed.").post()
            } catch {
                isImportingPresentation = false
                presentationImportError = presentationImportFailureMessage(
                    errorDescription: "That presentation could not be read.",
                    retainsCurrentPresentation: !importedSlides.isEmpty
                )
                AccessibilityNotification.Announcement("Presentation import failed.").post()
            }
        }
    }

    private func resetCustomTimingEvenly() {
        guard !importedSlides.isEmpty,
              let plan = try? buildTimedDeckPlan(
                title: importedFileName ?? "Presentation",
                slides: importedSlides,
                allocation: .even(totalSeconds: targetDurationSeconds)
              ) else {
            customSlideDurations = []
            return
        }
        var previousTarget = 0
        customSlideDurations = plan.checkpoints.map { checkpoint in
            defer { previousTarget = checkpoint.targetCumulativeSeconds }
            return checkpoint.targetCumulativeSeconds - previousTarget
        }
    }

    private func removePresentation() {
        if name == autoGeneratedSessionName {
            name = "Practice Session"
        }
        autoGeneratedSessionName = nil
        importedFileName = nil
        importedSlides = []
        customSlideDurations = []
        presentationImportError = nil
    }

