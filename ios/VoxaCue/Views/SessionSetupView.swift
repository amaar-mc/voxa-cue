                if emergencyBuzzerEnabled {
                    Label(
                        emergencyBuzzerReadinessText,
                        systemImage: emergencyBuzzerFirmwareSupported
                            ? "info.circle.fill"
                            : "exclamationmark.circle"
                    )
                    .font(.cueCaption.weight(.semibold))
                    .foregroundStyle(emergencyBuzzerFirmwareSupported ? CueTheme.signal : CueTheme.red)
                    .transition(.opacity.combined(with: .move(edge: .top)))
                }
            }
            .animation(.easeOut(duration: 0.18), value: emergencyBuzzerEnabled)
        }
    }

    private var preflightCard: some View {
        PremiumCard(padding: 20) {
            VStack(alignment: .leading, spacing: 14) {
                CueSectionLabel(text: "Preflight", color: CueTheme.signal)
                preflightRow(
                    label: model.demoMode ? "Audio source" : "Phone microphone",
                    detail: model.demoMode ? "Deterministic simulation" : "Permission checked when you begin",
                    state: model.demoMode ? .ready : .pending
                )
                preflightRow(label: "Cue Band", detail: bandPreflightDetail, state: bandPreflightState)
                if sessionMode == .powerPoint {
                    preflightRow(
                        label: "Presentation",
                        detail: presentationPreflightDetail,
                        state: preparedDeckPlan == nil ? .required : .ready
                    )
                }
                if !isCueReady {
                    Button {
                        model.connectCueBand()
                    } label: {
                        Label("Connect Cue Band", systemImage: "wave.3.right")
                            .font(.system(.subheadline, design: .rounded, weight: .semibold))
                            .frame(maxWidth: .infinity, minHeight: 44)
                    }
                    .buttonStyle(.bordered)
                    .tint(CueTheme.signal)
                    .disabled(bandConnectionIsBusy)
                }
            }
        }
    }

    private func preflightRow(label: String, detail: String, state: PreflightState) -> some View {
        HStack(alignment: .top, spacing: 11) {
            Image(systemName: state.symbol)
                .font(.system(size: 15, weight: .semibold))
                .foregroundStyle(state.color)
                .frame(width: 22)
            VStack(alignment: .leading, spacing: 2) {
                Text(label)
                    .font(.system(.subheadline, design: .rounded, weight: .semibold))
                    .foregroundStyle(CueTheme.ink)
                Text(detail)
                    .font(.cueCaption)
                    .foregroundStyle(CueTheme.secondaryInk)
                    .fixedSize(horizontal: false, vertical: true)
            }
            Spacer(minLength: 4)
        }
        .accessibilityElement(children: .combine)
    }

    private var isCueReady: Bool {
        if case .ready = model.connectionState { return true }
        return false
    }

    private var bandConnectionIsBusy: Bool {
        switch model.connectionState {
        case .searching, .connecting, .discovering, .reconnecting: true
        default: false
        }
    }

    private var bandPreflightState: PreflightState {
        if isCueReady, emergencyBuzzerEnabled, !emergencyBuzzerFirmwareSupported {
            return .required
        }
        if isCueReady {
            return enabledCues.isEmpty && !emergencyBuzzerEnabled ? .optional : .ready
        }
        if bandConnectionIsBusy { return .pending }
        if emergencyBuzzerEnabled { return .required }
        return .optional
    }

    private var bandPreflightDetail: String {
        if isCueReady, emergencyBuzzerEnabled, !emergencyBuzzerFirmwareSupported {
            return "Connected · firmware 1.3 required"
        }
        if isCueReady, enabledCues.isEmpty, emergencyBuzzerEnabled {
            return "Connected for the overtime buzzer"
        }
        if isCueReady, enabledCues.isEmpty { return "Connected · all band cues are off" }
        if isCueReady, emergencyBuzzerEnabled { return "Connected for haptics and overtime buzzer" }
        if isCueReady { return "Connected for live haptics" }
        if bandConnectionIsBusy { return model.connectionState.label }
        if emergencyBuzzerEnabled { return "Required for the emergency buzzer" }
        if sessionMode == .powerPoint,
           let presentationDetail = disconnectedPresentationBandDetail(
               transitionCueEnabled: transitionCueEnabled
           ) {
            return presentationDetail
        }
        return "Optional · analytics continue without it"
    }

    private var paceRangeIsValid: Bool {
        minimumWPM < maximumWPM
    }

    private var willUseCueBand: Bool {
        isCueReady && (!enabledCues.isEmpty || emergencyBuzzerEnabled)
    }

    private var beginButtonTitle: String {
        if sessionMode == .powerPoint {
            return "Begin presentation"
        }
        return willUseCueBand ? "Begin with Cue Band" : "Begin analytics only"
    }

    private var startDisabled: Bool {
        startDisabledReason != nil
    }

    private var startDisabledReason: String? {
        if name.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty {
            return "Add a session name to continue."
        }
        if !paceRangeIsValid {
            return "Set a valid pace range to continue."
        }
        if sessionMode == .powerPoint, isImportingPresentation {
            return "Wait for the presentation to finish importing."
        }
        if sessionMode == .powerPoint, importedSlides.isEmpty {
            return "Choose a PowerPoint (.pptx) or PDF to continue."
        }
        if sessionMode == .powerPoint, timingMode == .perSlide, !timingTotalIsValid {
            return "Slide times must add up to \(TimeInterval(targetDurationSeconds).clockString)."
        }
        if sessionMode == .powerPoint, preparedDeckPlan == nil {
            return "Increase the target time so every slide receives at least one second."
        }
        if emergencyBuzzerEnabled, !isCueReady {
            return "Connect a Cue Band or turn off the emergency buzzer."
        }
        if emergencyBuzzerEnabled, !emergencyBuzzerFirmwareSupported {
            return "Flash Cue Band firmware 1.3 or turn off the emergency buzzer."
        }
        return nil
    }

    private var emergencyBuzzerFirmwareSupported: Bool {
        guard isCueReady, let status = model.lastBandStatus else { return false }
        return CueBLE.supportsEmergencyBuzzer(
            firmwareMajor: status.firmwareMajor,
            firmwareMinor: status.firmwareMinor
        )
    }

    private var emergencyBuzzerReadinessText: String {
        if emergencyBuzzerFirmwareSupported {
            return "Firmware supports D9 · test the buzzer before presenting"
        }
        if isCueReady {
            return "Connected band requires firmware 1.3 or newer"
        }
        return "Connect Cue Band before you begin"
    }

    private func cueBinding(_ cue: CueKind) -> Binding<Bool> {
        Binding(
            get: { enabledCues.contains(cue) },
            set: { enabled in
                model.setCueEnabled(cue, enabled: enabled)
            }
        )
    }

    private var enabledCues: Set<CueKind> {
        var cues = model.hapticPreferences.enabledCues
        if sessionMode == .powerPoint, transitionCueEnabled {
            cues.insert(.deckBehind)
        } else {
            cues.remove(.deckBehind)
        }
        return cues
    }

    private var presentationPreflightDetail: String {
        guard !importedSlides.isEmpty else { return "No file selected" }
        let timing = timingMode == .even ? "simple timing" : "advanced timing"
        return "\(importedSlides.count) slides · \(timing) · on-device"
    }

    private func symbol(for cue: CueKind) -> String {
        switch cue {
        case .tooFast: "hare"
        case .tooSlow: "tortoise"
        case .fillerBurst: "ellipsis.bubble"
        case .deckBehind: "rectangle.stack.badge.play"
        case .time50, .time75, .time90, .time100: "timer"
        }
    }

    private func patternDescription(for cue: CueKind) -> String {
        let pattern = model.hapticPreferences.patternByCue[cue] ?? .doubleTap
        let intensity = model.hapticPreferences.intensityByCue[cue] ?? .medium
        if cue == .fillerBurst {
            let cluster = model.hapticPreferences.fillerClusterConfiguration
            return "\(pattern.label) · \(intensity.label) · \(cluster.requiredFillerCount) in \(cluster.windowSeconds) sec"
        }
        return "\(pattern.label) · \(intensity.label)"
    }

    private func begin() {
        let deckPlan = sessionMode == .powerPoint ? preparedDeckPlan : nil
        guard sessionMode != .powerPoint || deckPlan != nil else { return }
        let baseProfile = CoachingProfile.rehearsalV1()
        let haptics = model.hapticPreferences
        let profile = CoachingProfile(
            minimumWPM: minimumWPM,
            maximumWPM: maximumWPM,
            enabledCues: enabledCues,
            patternByCue: haptics.patternByCue,
            intensityByCue: haptics.intensityByCue,
            fillerClusterConfiguration: haptics.fillerClusterConfiguration,
            highConfidenceFillers: baseProfile.highConfidenceFillers,
            optionalFillers: baseProfile.optionalFillers
        )
        let configuration = SessionConfiguration(
            id: UUID(),
            name: name.trimmingCharacters(in: .whitespacesAndNewlines),
            mode: sessionMode,
            targetDurationSeconds: TimeInterval(targetDurationSeconds),
            profile: profile,
            deckPlan: deckPlan,
            emergencyBuzzerEnabled: emergencyBuzzerEnabled
        )
        model.beginSession(configuration: configuration)
    }
}

private enum PresentationTimingMode: String, CaseIterable {
    case even
    case perSlide
}

private enum PreflightState {
    case ready
    case pending
    case required
    case optional

    var symbol: String {
        switch self {
        case .ready: "checkmark.circle.fill"
        case .pending: "clock.fill"
        case .required: "exclamationmark.circle.fill"
        case .optional: "minus.circle.fill"
        }
    }

    var color: Color {
        switch self {
        case .ready: CueTheme.green
        case .pending: CueTheme.signal
        case .required: CueTheme.red
        case .optional: CueTheme.secondaryInk
        }
    }
}
