func presentationImportFailureMessage(
    errorDescription: String,
    retainsCurrentPresentation: Bool
) -> String {
    let trimmedDescription = errorDescription.trimmingCharacters(in: .whitespacesAndNewlines)
    guard retainsCurrentPresentation else { return trimmedDescription }
    let sentence = trimmedDescription.hasSuffix(".")
        ? trimmedDescription
        : "\(trimmedDescription)."
    return "\(sentence) Your current presentation is unchanged."
}

func simplePresentationTimingDescription(
    targetDurationSeconds: Int,
    slideCount: Int
) -> String {
    guard slideCount > 0 else { return "Divide the target evenly" }
    guard targetDurationSeconds >= slideCount else {
        let minimumMinutes = Int(ceil(Double(slideCount) / 60))
        return "Increase target to at least \(minimumMinutes) min"
    }
    let average = Double(targetDurationSeconds) / Double(slideCount)
    return "Divide evenly · about \(TimeInterval(average).clockString) per slide"
}

func disconnectedPresentationBandDetail(transitionCueEnabled: Bool) -> String? {
    transitionCueEnabled
        ? "Not connected · on-screen guide continues without wrist cues"
        : nil
}

