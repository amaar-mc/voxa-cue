func boundedSessionName(_ name: String) -> String {
    String(name.prefix(80))
}

func presentationSessionName(from url: URL) -> String {
    boundedSessionName(url.deletingPathExtension().lastPathComponent)
}
import VoxaRuntime

func presentationImportFailureMessage(
    errorDescription: String,
    retainsCurrentPresentation: Bool
) -> String {
    let trimmedDescription = errorDescription.trimmingCharacters(in: .whitespacesAndNewlines)
    guard retainsCurrentPresentation else { return trimmedDescription }
    let sentence = trimmedDescription.hasSuffix(".")
        ? trimmedDescription
        : "\(trimmedDescription)."
    return "\(sentence) Your current presentation is unchanged."
}

func simplePresentationTimingDescription(
    targetDurationSeconds: Int,
    slideCount: Int
) -> String {
    guard slideCount > 0 else { return "Divide the target evenly" }
    guard targetDurationSeconds >= slideCount else {
        let minimumMinutes = Int(ceil(Double(slideCount) / 60))
        return "Increase target to at least \(minimumMinutes) min"
    }
    let average = Double(targetDurationSeconds) / Double(slideCount)
    return "Divide evenly · about \(TimeInterval(average).clockString) per slide"
}

enum SpeakingPacePreset: String, CaseIterable, Equatable {
    case slow
    case normal
    case fast

    var label: String {
        switch self {
        case .slow: "Slow"
        case .normal: "Normal"
        case .fast: "Fast"
        }
    }

    var range: ClosedRange<Double> {
        switch self {
        case .slow: 100...130
        case .normal: 130...160
        case .fast: 160...190
        }
    }

    static func matching(minimumWPM: Double, maximumWPM: Double) -> SpeakingPacePreset? {
        allCases.first { preset in
            preset.range.lowerBound == minimumWPM && preset.range.upperBound == maximumWPM
        }
    }
}

func hapticPatternPulseDescription(_ pattern: HapticPattern) -> String {
    switch pattern {
    case .doubleTap: "2 short pulses"
    case .longPulse: "1 long pulse"
    case .tripleTap: "3 quick pulses"
    case .longShortLong: "Long, short, long pulses"
    case .singlePulse: "1 firm pulse"
    case .doublePulse: "2 firm pulses"
    case .triplePulse: "3 firm pulses"
    case .calmWave: "1 gradual pulse"
    case .deadlineHold: "1 sustained pulse"
    }
}

